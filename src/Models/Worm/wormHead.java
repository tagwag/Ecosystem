package Models.Worm;

public class wormHead {

    public int x;
    public int y;
    public int age;

    public wormHead(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
